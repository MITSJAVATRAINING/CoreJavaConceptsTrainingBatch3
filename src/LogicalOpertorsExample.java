
public class LogicalOpertorsExample {
	public static void main(String args[]) {
		int a = 20;
		
		if (a < 10) {   // true
			System.out.println("The Value of a is 10");
		} else if (a == 10) {
			System.out.println("The Value of a is 10");
		} else {
			System.out.println("The Value of a is greater than 10");
		}
	}
}
